void oled_render_status (void) {
	oled_write_ln_P(PSTR(""), false);
	oled_write_ln_P(PSTR(" - KEYBOARD STATUS - "), false);
	
    // Keyboard Layer Status
    oled_write_P(PSTR("Layer: "), false);

    switch (get_highest_layer(layer_state)) {
        case 0:
            oled_write_P(PSTR("0 - Default\n"), false);
            break;
        case 1:
            oled_write_P(PSTR("1 - FN(1)\n"), false);
            break;
        case 2:
            oled_write_P(PSTR("2 - FN(2)\n"), false);
            break;
        default:
            // Or use the write_ln shortcut over adding '\n' to the end of your string
            oled_write_ln_P(PSTR("Undefined"), false);
    }
    oled_write_ln_P(PSTR(""), false);
    // Host Keyboard LED Status
    led_t led_state = host_keyboard_led_state();
	oled_write_P(PSTR("NumLock:  "), false);
    oled_write_ln_P(led_state.num_lock ?    PSTR("ON") : PSTR("   OFF"), false);
	oled_write_P(PSTR("Capslock: "), false);
    oled_write_ln_P(led_state.caps_lock ?   PSTR("ON") : PSTR("   OFF"), false);
	oled_write_P(PSTR("Scrlock:  "), false);
    oled_write_ln_P(led_state.scroll_lock ? PSTR("ON") : PSTR("   OFF"), false);
}

